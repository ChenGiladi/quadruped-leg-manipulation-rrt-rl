# Colorblind-safe palette (Wong) + line-style differentiation, so the two
# series remain distinguishable in grayscale print:
#   measured signal -> solid vermillion (#D55E00)
#   fitted sinusoid -> dashed near-black (#222222)
MEASURED_COLOR = "#D55E00"
FIT_COLOR = "#222222"
TRANSIENT_SHADE = "#f2f2f2"

# Resolve a stable relative path to the bundled signal data: this script lives
# at supplementary/code/python/, and the data lives at supplementary/data/.
SINE_DATA_DIR = Path(__file__).resolve().parents[2] / "data" / "sine_wave"


def _sine_panel(ax, npz_path, panel_title, xlim, ylim):
    d = np.load(npz_path)
    x = d["x"].astype(float)
    y = d["y"].astype(float)
    A = float(d["A"])
    omega = float(d["omega"])
    phi = float(d["phi"])
    c = float(d["c"])
    r2 = float(d["r2"])
    rmse = float(d["rmse"])
    t_fit_start = float(d["t_fit_start"])
    t_fit_end = float(d["t_fit_end"])

    # Shade the transient (pre-fit) region so reviewers can see what was
    # excluded from the parameter estimation.
    if t_fit_start > x.min():
        ax.axvspan(x.min(), t_fit_start, color=TRANSIENT_SHADE, zorder=0)
        ax.text(t_fit_start + 0.005 * (xlim[1] - xlim[0]),
                ylim[0] + 0.04 * (ylim[1] - ylim[0]),
                "transient\n(excluded)", fontsize=7.0, ha="left", va="bottom",
                color="#888888", family="serif")

    # Evaluate the fitted model on a uniform dense grid over the fit window
    t_dense = np.linspace(t_fit_start, t_fit_end, 1500)
    y_fit_dense = A * np.sin(omega * t_dense + phi) + c

    ax.plot(x, y, color=MEASURED_COLOR, linewidth=1.0, label="Measured",
            zorder=2)
    ax.plot(t_dense, y_fit_dense, color=FIT_COLOR, linestyle="--",
            linewidth=1.4, label=r"Fit: $A\sin(\omega t+\varphi)+c$",
            zorder=3)

    ax.set_xlim(*xlim)
    ax.set_ylim(*ylim)
    ax.set_xlabel(r"Time $t$ [s]", fontsize=10)
    ax.set_ylabel(r"Box coordinate $q(t)$ [sim. units]", fontsize=10)
    ax.set_title(panel_title, fontsize=10)
    ax.tick_params(axis="both", labelsize=9)
    ax.grid(True, axis="y", linestyle="-", color="#dddddd", linewidth=0.5, alpha=0.9)
    for side in ("top", "right"):
        ax.spines[side].set_visible(False)
    ax.spines["left"].set_linewidth(0.8)
    ax.spines["bottom"].set_linewidth(0.8)

    # Parameter annotation, compact and unboxed, upper-right (above the
    # bottom-of-axis legend; sits in negative half-cycle gaps)
    period = 2 * np.pi / omega
    note = ("\n".join([
        rf"$A={A:.2f}$,  $c={c:.2f}$",
        rf"$\omega={omega*1000:.3f}\times10^{{-3}}$ rad/s",
        rf"$T \approx {period/1000:.1f}\times10^{{3}}$ s",
        rf"$\varphi={phi:+.2f}$ rad",
        rf"$R^{{2}}={r2:.2f}$,  RMSE $={rmse:.2f}$",
    ]))
    ax.text(0.985, 0.97, note, transform=ax.transAxes, fontsize=7.8,
            ha="right", va="top", family="serif", color="#222222",
            linespacing=1.30,
            bbox=dict(boxstyle="round,pad=0.25", facecolor="white",
                      edgecolor="#cccccc", linewidth=0.5, alpha=0.92))


def render_sine(outdir: Path):
    plt.rcParams.update({
        "font.family": "serif",
        "font.serif": ["DejaVu Serif", "Liberation Serif", "Times New Roman"],
        "mathtext.fontset": "dejavuserif",
        "axes.titlesize": 10,
        "savefig.dpi": 300,
        "savefig.bbox": "tight",
    })

    # Determine shared axis limits across both panels for visual comparability
    l = np.load(SINE_DATA_DIR / "sine_curve_l.npz")
    r = np.load(SINE_DATA_DIR / "sine_curve_r.npz")
    x_max = float(max(l["x"].max(), r["x"].max()))
    y_min = float(min(l["y"].min(), r["y"].min()))
    y_max = float(max(l["y"].max(), r["y"].max()))
    pad = 0.05 * (y_max - y_min)
    xlim = (0.0, x_max * 1.02)
    ylim = (y_min - pad, y_max + pad)

    fig, axes = plt.subplots(1, 2, figsize=(9.6, 4.0), constrained_layout=True)
    _sine_panel(axes[0], SINE_DATA_DIR / "sine_curve_l.npz",
                "Left-circle pushing (counterclockwise)", xlim, ylim)
    _sine_panel(axes[1], SINE_DATA_DIR / "sine_curve_r.npz",
                "Right-circle pushing (clockwise)", xlim, ylim)

    # Single, shared, unboxed legend below the panels
    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=2,
               frameon=False, fontsize=9, bbox_to_anchor=(0.5, -0.04))

    out = outdir / "Sine_Waves.png"
    fig.savefig(out)
    plt.close(fig)
    print(f"wrote {out}")


