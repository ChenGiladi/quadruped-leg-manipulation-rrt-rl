# CLI
# =============================================================================
def main():
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--figure",
                        choices=("r2", "sine",
                                 "straight_gallery", "bezier_panelb", "all"),
                        default="all",
                        help="Which figure to regenerate. 'all' covers the "
                             "data-driven figures (r2, sine); the video "
                             "galleries are opt-in by explicit name.")
    args = parser.parse_args()

    here = Path(__file__).resolve()
    # Outputs land in <repo>/figures/, four levels up from this script
    out = here.parents[3] / "figures"
    if not out.exists():
        raise SystemExit(f"figures/ not found at {out}")

    if args.figure in ("r2", "all"):
        render_r2(out)
    if args.figure in ("sine", "all"):
        render_sine(out)
    if args.figure == "straight_gallery":
        src = VIDEOS_DIR / "03_Straight_Line_Box_Pushing.mp4"
        if not src.exists():
            raise SystemExit(f"video not found: {src}")
        render_straight_gallery(out, src)
    if args.figure == "bezier_panelb":
        src = VIDEOS_DIR / "02_Bezier_Trajectory_Box_Pushing.mp4"
        if not src.exists():
            raise SystemExit(f"video not found: {src}")
        render_bezier_panelb(out, src)


if __name__ == "__main__":
    main()
