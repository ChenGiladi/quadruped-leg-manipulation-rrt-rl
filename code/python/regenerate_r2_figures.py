# Videos live two levels up from this script under supplementary/videos/
VIDEOS_DIR = Path(__file__).resolve().parents[2] / "videos"

# Viewport of the straight-push video that excludes the CoppeliaSim scene-
# hierarchy panel on the left (so robot-tracking only searches inside the 3-D
# viewport, never matches yellow widgets in the panel).
STRAIGHT_VIEWPORT = (320, 0, 1280, 458)  # (x0, y0, x1, y1)

# Path to a TrueType font with consistent metrics across systems.
_FONT_CANDIDATES = [
    "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf",
    "/usr/share/fonts/TTF/DejaVuSans-Bold.ttf",
    "/Library/Fonts/Arial Bold.ttf",
    "C:/Windows/Fonts/arialbd.ttf",
]


def _load_font(size):
    for p in _FONT_CANDIDATES:
        if Path(p).exists():
            return ImageFont.truetype(p, size)
    return ImageFont.load_default()


def _extract_frame(video_path: Path, t_seconds: float) -> Image.Image:
    """Return a single decoded frame at ``t_seconds`` as a PIL RGB image.
    Uses ffmpeg with ``-ss`` before ``-i`` for fast keyframe seek; for short
    clips this is accurate enough for the purposes of a figure gallery.
    """
    cmd = ["ffmpeg", "-y", "-ss", f"{t_seconds:.3f}", "-i", str(video_path),
           "-frames:v", "1", "-f", "image2", "-c:v", "png", "pipe:1"]
    res = subprocess.run(cmd, capture_output=True, check=True)
    return Image.open(io.BytesIO(res.stdout)).convert("RGB")


def _yellow_centroid(img: Image.Image, viewport):
    """Centroid (cx, cy) in image coordinates of bright-yellow pixels
    restricted to ``viewport`` (Spot's chassis colour). Returns None if no
    yellow pixels are found.
    """
    x0, y0, x1, y1 = viewport
    arr = np.asarray(img.crop((x0, y0, x1, y1)))
    r = arr[:, :, 0].astype(int)
    g = arr[:, :, 1].astype(int)
    b = arr[:, :, 2].astype(int)
    mask = (r > 180) & (g > 130) & (b < 110) & ((r - b) > 80)
    ys, xs = np.where(mask)
    if xs.size == 0:
        return None
    return int(xs.mean()) + x0, int(ys.mean()) + y0


def _smart_crop(img: Image.Image, cx: int, cy: int, crop_w: int, crop_h: int,
                viewport) -> Image.Image:
    """Crop ``crop_w x crop_h`` centred on (cx, cy), clamped inside ``viewport``
    so the scene-hierarchy panel never sneaks back in."""
    vx0, vy0, vx1, vy1 = viewport
    half_w, half_h = crop_w // 2, crop_h // 2
    x0 = max(vx0, min(cx - half_w, vx1 - crop_w))
    y0 = max(vy0, min(cy - half_h, vy1 - crop_h))
    return img.crop((x0, y0, x0 + crop_w, y0 + crop_h))


def _stamp_timestamp(img: Image.Image, t_seconds: float,
                     font_height_frac: float = 0.08,
                     margin_frac: float = 0.04,
                     pos: str = "bottom-left") -> None:
    """Stamp ``t = X.X s`` in a black chip with white text in ``img``.

    The font size auto-scales as a fraction of image height, and the chip is
    inset from the image edges by a fraction of the smaller dimension.  This
    makes the printed appearance consistent across panels even when the source
    panels have very different pixel resolutions (e.g. panel a at 500x350 vs
    panel b at 1280x800 -- without auto-scaling the same pixel font would
    print at very different physical sizes after the LaTeX scale-to-width).
    """
    W, H = img.size
    font_size = max(10, int(H * font_height_frac))
    margin = max(4, int(min(W, H) * margin_frac))
    font = _load_font(font_size)
    draw = ImageDraw.Draw(img)
    txt = f"t = {t_seconds:.1f} s"
    # PIL's textbbox origin is offset from the draw anchor because glyph
    # metrics include leading space (ascent gap, side bearings).  We need both
    # the visual width/height (tw, th) for sizing the chip AND the offsets
    # (bx0, by0) so we can shift the draw call and put the GLYPH -- not the
    # font's metric box -- at (x_box + pad, y_box + pad) inside the chip.
    # Without subtracting (bx0, by0) the text rides low / off-centre in the
    # black rectangle, which is what the rendered Fig 17 was showing.
    bx0, by0, bx1, by1 = draw.textbbox((0, 0), txt, font=font)
    tw, th = bx1 - bx0, by1 - by0
    pad = max(4, font_size // 5)
    if pos == "bottom-left":
        x_box, y_box = margin, H - th - 2 * pad - margin
    elif pos == "top-right":
        x_box, y_box = W - tw - 2 * pad - margin, margin
    else:
        x_box, y_box = margin, margin
    draw.rectangle((x_box, y_box, x_box + tw + 2 * pad,
                    y_box + th + 2 * pad), fill="black")
    draw.text((x_box + pad - bx0, y_box + pad - by0),
              txt, fill="white", font=font)


def _mosaic(panels, cols: int, rows: int, gap: int = 4,
            bg: str = "white") -> Image.Image:
    pw, ph = panels[0].size
    W = pw * cols + gap * (cols + 1)
    H = ph * rows + gap * (rows + 1)
    sheet = Image.new("RGB", (W, H), bg)
    for i, p in enumerate(panels):
        r, c = divmod(i, cols)
        x = gap + c * (pw + gap)
        y = gap + r * (ph + gap)
        sheet.paste(p, (x, y))
    return sheet


def render_straight_gallery(outdir: Path, src_video: Path):
    """Figure 17a: eight zoomed timestamped frames of straight-line pushing."""
    timestamps = [5.0, 18.0, 32.0, 46.0, 60.0, 74.0, 88.0, 102.0]
    crop_w, crop_h = 500, 350
    panels = []
    for t in timestamps:
        frame = _extract_frame(src_video, t)
        centroid = _yellow_centroid(frame, STRAIGHT_VIEWPORT)
        if centroid is None:
            # Fallback to viewport centre — robot temporarily out of view.
            vx0, vy0, vx1, vy1 = STRAIGHT_VIEWPORT
            centroid = ((vx0 + vx1) // 2, (vy0 + vy1) // 2)
        cropped = _smart_crop(frame, centroid[0], centroid[1],
                              crop_w, crop_h, STRAIGHT_VIEWPORT)
        _stamp_timestamp(cropped, t)
        panels.append(cropped)
    sheet = _mosaic(panels, cols=4, rows=2, gap=4)
    out = outdir / "Straight_Push_Gallery.png"
    sheet.save(out, dpi=(300, 300))
    print(f"wrote {out} ({sheet.size})")


def render_bezier_panelb(outdir: Path, src_video: Path):
    """Figure 17b: eight close-up timestamped frames of one-leg Bezier-curve
    pushing. The source video is already framed tightly on the leg + box, so
    a static centre crop suffices and no robot tracking is needed."""
    timestamps = [0.5, 1.5, 2.5, 3.5, 4.5, 5.5, 6.5, 7.5]
    crop_w, crop_h = 1280, 800
    panels = []
    for t in timestamps:
        frame = _extract_frame(src_video, t)
        W, H = frame.size
        x0 = max(0, (W - crop_w) // 2)
        y0 = max(0, (H - crop_h) // 2)
        cropped = frame.crop((x0, y0, x0 + crop_w, y0 + crop_h))
        _stamp_timestamp(cropped, t)
        panels.append(cropped)
    sheet = _mosaic(panels, cols=4, rows=2, gap=4)
    out = outdir / "Robodog_Push.png"
    sheet.save(out, dpi=(300, 300))
    print(f"wrote {out} ({sheet.size})")


