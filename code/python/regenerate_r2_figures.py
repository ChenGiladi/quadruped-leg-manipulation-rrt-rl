MARKER_FACE = "#1f4e79"
MARKER_EDGE = "#0b1f33"
FIT_LINE = "#333333"


def linear_fit_nonneg(x, y):
    """Descriptive linear trend with intercept constrained to be >= 0.
    Returns (slope, intercept, r2). Avoids implying nonphysical negative
    error at low mass, since OLS on a few points can produce a negative
    intercept that has no physical meaning for an error metric.
    """
    slope_ols, intercept_ols = np.polyfit(x, y, 1)
    if intercept_ols >= 0:
        slope, intercept = slope_ols, intercept_ols
    else:
        # Refit with intercept fixed at 0: y = s*x; s = (x.y) / (x.x)
        slope = float(np.dot(x, y) / np.dot(x, x))
        intercept = 0.0
    y_hat = slope * x + intercept
    ss_res = np.sum((y - y_hat) ** 2)
    ss_tot = np.sum((y - y.mean()) ** 2)
    r2 = 1.0 - ss_res / ss_tot
    return slope, intercept, r2


def r2_panel(ax, x, y, ylabel, unit_label, slope_unit_short):
    slope, intercept, r2 = linear_fit_nonneg(x, y)
    x_fit = np.linspace(x.min(), x.max(), 200)
    y_fit = slope * x_fit + intercept

    ax.plot(x_fit, y_fit, linestyle="--", linewidth=1.6, color=FIT_LINE, zorder=2)
    ax.scatter(x, y, s=70, facecolor=MARKER_FACE, edgecolor=MARKER_EDGE,
               linewidths=0.7, zorder=3)

    ax.set_xlim(-50, 850)
    ax.set_ylim(0, max(y.max() * 1.12, 1e-3))
    ax.set_xticks([0, 200, 400, 600, 800])
    ax.set_xlabel(r"Relative mass increase, $\Delta m / m_{\mathrm{train}}$ (%)", fontsize=10)
    ax.set_ylabel(ylabel + f" ({unit_label})", fontsize=10)
    ax.tick_params(axis="both", labelsize=9)
    ax.grid(True, axis="y", linestyle="-", color="#dddddd", linewidth=0.5, alpha=0.9)
    for side in ("top", "right"):
        ax.spines[side].set_visible(False)
    ax.spines["left"].set_linewidth(0.8)
    ax.spines["bottom"].set_linewidth(0.8)

    # Slope per 100% mass increase; compact, unboxed, in bottom-right
    slope_per_100 = slope * 100.0
    note = (f"slope: {slope_per_100:.2f} {slope_unit_short} per 100% mass increase\n"
            f"$R^2$: {r2:.2f}  (descriptive linear trend)")
    ax.text(0.97, 0.04, note, transform=ax.transAxes, fontsize=8.5,
            ha="right", va="bottom", family="serif", color="#222222")


def render_r2(outdir: Path):
    plt.rcParams.update({
        "font.family": "serif",
        "font.serif": ["DejaVu Serif", "Liberation Serif", "Times New Roman"],
        "mathtext.fontset": "dejavuserif",
        "axes.titlesize": 10,
        "savefig.dpi": 300,
        "savefig.bbox": "tight",
    })

    for path, x, y, ylabel, unit, slope_unit in [
        (outdir / "R_squared_Angle.png", DM_PCT, ANGULAR_ERR_DEG,
         "Angular error", r"$^{\circ}$", r"$^{\circ}$"),
        (outdir / "R_squared_Position.png", DM_PCT, POSITION_ERR_M,
         "Position error", "m", "m"),
    ]:
        fig, ax = plt.subplots(figsize=(5.2, 3.6))
        r2_panel(ax, x, y, ylabel, unit, slope_unit)
        fig.savefig(path)
        plt.close(fig)
        print(f"wrote {path}")


